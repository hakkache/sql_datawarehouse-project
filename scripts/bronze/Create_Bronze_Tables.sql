/*
===============================================================================
 Script:       Create_Bronze_Tables.sql
 Description:  Defines and initializes all base (Bronze layer) tables within 
               the 'bronze' schema. This script drops existing tables if they 
               already exist, then recreates them with the defined structure.

 Functionality:
   • Drops existing Bronze tables if they exist.
   • Creates source ingestion-level tables for CRM and ERP data.
   • Used to reset the schema structure during environment setup.

 Warning:
   ⚠️ Executing this script will DROP and recreate Bronze tables.
   Any existing data within these tables will be permanently deleted.

 Author:       HAKKACHE MOHAMED
 Created On:   10/28/2025
 Version:      1.0
===============================================================================
*/

-- =============================
-- CRM Customer Information
-- =============================
IF OBJECT_ID('bronze.crm_cust_info', 'U') IS NOT NULL
    DROP TABLE bronze.crm_cust_info;
GO

CREATE TABLE bronze.crm_cust_info (
    cst_id              INT,
    cst_key             NVARCHAR(50),
    cst_firstname       NVARCHAR(50),
    cst_lastname        NVARCHAR(50),
    cst_marital_status  NVARCHAR(50),
    cst_gndr            NVARCHAR(50),
    cst_create_date     DATE
);
GO

-- =============================
-- CRM Product Information
-- =============================
IF OBJECT_ID('bronze.crm_prd_info', 'U') IS NOT NULL
    DROP TABLE bronze.crm_prd_info;
GO

CREATE TABLE bronze.crm_prd_info (
    prd_id       INT,
    prd_key      NVARCHAR(50),
    prd_nm       NVARCHAR(50),
    prd_cost     INT,
    prd_line     NVARCHAR(50),
    prd_start_dt DATETIME,
    prd_end_dt   DATETIME
);
GO

-- =============================
-- CRM Sales Details
-- =============================
IF OBJECT_ID('bronze.crm_sales_details', 'U') IS NOT NULL
    DROP TABLE bronze.crm_sales_details;
GO

CREATE TABLE bronze.crm_sales_details (
    sls_ord_num  NVARCHAR(50),
    sls_prd_key  NVARCHAR(50),
    sls_cust_id  INT,
    sls_order_dt INT,
    sls_ship_dt  INT,
    sls_due_dt   INT,
    sls_sales    INT,
    sls_quantity INT,
    sls_price    INT
);
GO

-- =============================
-- ERP Location Data
-- =============================
IF OBJECT_ID('bronze.erp_loc_a101', 'U') IS NOT NULL
    DROP TABLE bronze.erp_loc_a101;
GO

CREATE TABLE bronze.erp_loc_a101 (
    cid    NVARCHAR(50),
    cntry  NVARCHAR(50)
);
GO

-- =============================
-- ERP Customer Data
-- =============================
IF OBJECT_ID('bronze.erp_cust_az12', 'U') IS NOT NULL
    DROP TABLE bronze.erp_cust_az12;
GO

CREATE TABLE bronze.erp_cust_az12 (
    cid    NVARCHAR(50),
